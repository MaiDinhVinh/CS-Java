CREATE TABLE Module(
    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    mod_name VARCHAR(255) NOT NULL,
    mod_duration INT NOT NULL,
    mod_level VARCHAR(255) NOT NULL,
    PRIMARY KEY (id)
);

