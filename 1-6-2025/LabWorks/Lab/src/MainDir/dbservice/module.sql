    id INT UNSIGNED NOT NULL AUTO_INCREMENT,
    teacher_name VARCHAR(255) NOT NULL,
    teacher_dob DATE NOT NULL,
    teacher_ssid VARCHAR(255) NOT NULL,
    PRIMARY KEY (id)
