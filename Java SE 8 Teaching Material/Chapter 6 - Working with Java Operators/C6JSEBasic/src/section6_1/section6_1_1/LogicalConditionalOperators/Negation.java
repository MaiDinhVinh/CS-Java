package section6_1.section6_1_1.LogicalConditionalOperators;

public class Negation {
    /**
     * {@code !} represents Negation<p>
     * This is Unary Operation (involves 1 operands)*/
    public static void main(String[] args) {
        boolean hello = false;
        boolean goodbye = !hello;
        System.out.println(goodbye); //true
    }
}
